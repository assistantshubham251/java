public class AssignmentOperator {
    public static void main(String args[]){
        int a = 45;
        a /= 5;
        System.out.println(a);
        int b = 7;
        b *= 8;
        System.out.println(b);
        int c = 3;
        c += 23;
        System.out.println(c);
        int d = 80;
        d -= 10;
        System.out.println(d);
    }
}
